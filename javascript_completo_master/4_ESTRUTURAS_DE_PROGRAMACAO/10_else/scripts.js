//Estrutura de condição IF(se)/ELSE(se não)

// declaração da variavel: velocidade que recebe 70
let velocidade = 70;
// (IF) se velocidade for menor ou igual a 80 faça e imprima
if(velocidade <= 80) {
  console.log("Não foi multado");
// (ELSE) se não faça isso e imprima
} else {
 console.log("Foi multado!");
}