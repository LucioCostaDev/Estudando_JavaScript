//Função built--in: Math.x() -> utilizada para expressões/calculos matematicos
// função nativo do jva script

// Função pra saber qual o maior número
let maior = Math.max(6,12,28,55,8); 
console.log(maior);

// Função qual o menor número
let menor = Math.min(6,12,28,55,8);
console.log(menor);

// Função de arendonda pra cima, caso tenha numero com casa decimal
let arredondar = Math.round(5.2934723);
console.log(arredondar);

// Função de arendondamento para baixo, caso tenha número com casa decimal
let arredondarParaCima = Math.ceil(5.2934723);
console.log(arredondarParaCima);