/* A função Math.pow exibe a pontencia de uma número 
 Teste a função com console.log() e as seguintes bases: 2,3,18 e o expoente deve ser 2
 Recebe 2 argumentos, base e expoente
 ex.: Math.pow(5,3) */

// // declaração da variavel num 
const num = 18;

// mostrar na tela os resultados das potencias com a função Math.pow (ao quadrado)
console.log(Math.pow(2,2)); // base 2 x 2 expoente = 4 resultado
console.log(Math.pow(3,2)); // base 3 x 3 expoente = 9 resultado
console.log(Math.pow(num,2)); // base 18 x 18 expoente = 324 resultado

// Obs. Pode ser utilizado tanto numero diretamente declarando dentro do console, como pode ser declarado varivale para receber  



