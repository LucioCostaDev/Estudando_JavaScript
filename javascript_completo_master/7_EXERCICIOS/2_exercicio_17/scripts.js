// criação da função que vai receber idade
function imprimeIdade(idade) {

  console.log(`Você tem ${idade} anos`);
}

imprimeIdade(12); // Você tem 12 anos 
imprimeIdade(28); // Vovê tem 28 anos
imprimeIdade(45); // Você tem 45 anos

