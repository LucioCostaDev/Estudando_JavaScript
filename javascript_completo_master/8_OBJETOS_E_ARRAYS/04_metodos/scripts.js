/* Métodos
Métodos são propriedades que funcionam como funções 
Strings e array contém métodos 
*/

//declaração de variavel 
let marca = 'nike';
// aqui e usado o metodo .toUpperCase() que coloca todas as letras em Maiusculos
console.log(marca.toUpperCase()); // NIKE
// declaração de varivel recebendo um metodo direto
let marca2 = marca.toUpperCase(); // nike
// aqui e usado o metodo ao contrario que deixa todas as letras minuscula
console.log(marca2.toLowerCase()); // function
//aqui o metodo typeof mostra que após esta sendo executado uma function
console.log(typeof marca2.toLowerCase);


