/* Métodos de array: reverse 
tem como função inverter a array, coverte qualquer tipo de dado 
*/

// declaração de array
let carros = ["BMW", "Fiat", "VW", "Renault", "Audi"];

// imprimir a inversão da array
console.log(carros.reverse()); // [ 'Audi', 'Renault', 'VW', 'Fiat', 'BMW' ]
// declaração de array nums
let nums = [1,2,3,45,6,7,7]; 
// imprimir a inversão da array nums
console.log(nums.reverse()); //  [ 7, 7, 6, 45, 3, 2, 1]