/* Arrays
Possibilidade de adicionar um conjunto de valores a uma variavel
O array deve ser escrito entre colchetes, separando os valores por virgulas, veja
*/

// declaração de array com números 
let arr = [1, 4, 5, 6, 7];
// declaração de array com string
let nomes = ['Matheus', 'João', 'Aléxia'];
// declaração de arrays com booleam
let bool = [true, false, true];
// declaração de array diversificado
let misturado = [1, 'Matheus', true];


console.log(arr); // [1, 4, 5, 6, 7 ]
console.log(nomes); // ['Matheus', 'João', 'Aléxia' ]
console.log(misturado); // [true, false, true]
console.log(bool); // [1, 'Matheus', true]